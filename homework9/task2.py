def Quadratic(a, b, c):
    """
    Решение квадратного уравнения c обработкой отрицательного дискриминанта.
    """
    d = b**2 - 4*a*c
    x = []
    if a == 0:
        x = []
    elif d > 0:
        x = [(- b + d**(0.5)) / (2*a), (- b - d**(0.5)) / (2*a)]
    elif d == 0:
        x = [- b / (2*a)]
    elif d < 0:
        x = [complex((- b + d**(0.5)) / (2*a)), complex((- b - d**(0.5)) / (2*a))]
    return x
